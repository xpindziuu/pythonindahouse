#flow control for 2

#from 12 to 20, adding 1 on every loop
for num in range(12, 21):
    print(num)
    
print('~~~~~~~~~~~~~~~~~~~~~~~~')

#from 12 to 20, adding 2 on every loop
for num in range(12, 21, 2):
    print(num)

print('~~~~~~~~~~~~~~~~~~~~~~~~')

#from 20 to 12, subtracting 1 on every loop
for num in range(20, 12, -1):
    print(num)